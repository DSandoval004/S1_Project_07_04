// classes
function navClasses() {
    document.getElementById('NavHome').style.display = 'block'
}

function NavClasses() {
    document.getElementById('navHome').style.display = 'none'
}
// camps
function navCamps() {
    document.getElementById('navCamps').style.color = 'white'
    document.getElementById('NavCamps').style.backgroundColor = 'blue'
}

function NavCamps() {
    document.getElementById('navCamps').style.color = 'black'
}
// PullDown
function pullDownClasses() {
    document.getElementById('classes').style.display = 'block'
}

function pullUpClasses() {
    document.getElementById('classes').style.display = 'none'
}